/** Map from ship ID (journal name) to ShipData for O(1) lookups. */
export const SHIP_BY_ID: ReadonlyMap<string, ShipData> = new Map(
  SHIPS.map((s) => [s.id, s]),
);

/**
 * Get ship data by journal ID.
 * Returns undefined if the ID is not recognized.
 */
export function getShipById(id: string): ShipData | undefined {
  return SHIP_BY_ID.get(id);
}

/**
 * Get ship data by display name (case-insensitive).
 * Returns undefined if no match is found.
 */
export function getShipByName(displayName: string): ShipData | undefined {
  const lower = displayName.toLowerCase();
  return SHIPS.find((s) => s.displayName.toLowerCase() === lower);
}
