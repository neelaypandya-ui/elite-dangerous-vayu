// ---------------------------------------------------------------------------
// Ship Data Interface
// ---------------------------------------------------------------------------

/** Static reference data for a single ship model. */
export interface ShipData {
  /** Internal ED journal name (e.g. 'SideWinder', 'CobraMkIII'). */
  id: string;
  /** Human-readable display name (e.g. 'Sidewinder Mk I'). */
  displayName: string;
  /** Ship manufacturer. */
  manufacturer: string;
  /** Landing pad size class. */
  class: 'Small' | 'Medium' | 'Large';
  /** Base purchase cost in credits (unengineered, stock). */
  cost: number;
  /** Approximate maximum jump range in LY (fully engineered). */
  maxJumpRange: number;
  /** Weapon hardpoint counts by size. */
  hardpoints: { small: number; medium: number; large: number; huge: number };
  /** Number of optional internal module slots. */
  optionalSlots: number;
  /** Total crew seats including pilot (1 = solo only). */
  crewSeats: number;
  /** Maximum cargo capacity in tons (largest racks, no shield). */
  maxCargo: number;
  /** Subjective combat rating (1-10). */
  combatRating: number;
  /** Subjective exploration rating (1-10). */
  explorationRating: number;
  /** Subjective trading/hauling rating (1-10). */
  tradingRating: number;
  /** Subjective mining rating (1-10). */
  miningRating: number;
}

